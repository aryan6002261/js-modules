let a = 5;
let b = 10;
console.log(a + b); // 15
console.log(a * b); // 50
console.log(a / b); // 0.5
console.log(a - b); // -5
console.log(a % b); // 5
console.log(a ** 2); // 25
console.log(a++); // 5
console.log(++a); // 7
console.log(b--); // 10
console.log(--b); // 8
console.log(a == b); // false
console.log(a != b); // true
console.log(a > b); // false
console.log(a < b); // true
console.log(a >= b); // false
console.log(a <= b); // true
let c = a > b ? a : b;
console.log(c); // 10