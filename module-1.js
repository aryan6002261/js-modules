// ------------------------------------------
// Single-Line Comment
// Use // to write a comment on one line.
// Comments are ignored by JavaScript.
// ------------------------------------------

// This line prints a text message.
console.log("Hello, World!");

// This line prints the result of an arithmetic expression.
console.log(2 + 2);

// This line prints a Boolean value.
console.log(true);

/*
--------------------------------------------
Multi-Line Comment
Use this type of comment when your explanation
spans multiple lines.
JavaScript ignores everything inside this block.
--------------------------------------------
*/

// Print your name
console.log("My name is Alex");

// Print your favorite number
console.log(25);

// Print whether you like JavaScript
console.log(true);